def ttc(num):
    if num==0:
        return('白色')
    elif num==1:
        return('黄色')
    elif num==2:
        return('蓝色')
    elif num==3:
        return('绿色')
    elif num==4:
        return('橙色')
    elif num==5:
        return('紫色')
    elif num==6:
        return('红色')
    else:
        return('空')